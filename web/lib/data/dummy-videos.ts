export function getDummyVideos() {
  return dummyVideos
}

export function getDummyVideoById(videoId: Video["id"]) {
  return dummyVideos.find((video) => video.id === videoId) ?? null
}

export function getDummyVideosByOwner(userId: User["id"]) {
  return dummyVideos.filter((video) => video.userId === userId)
}

export function getDummyUploadSessionsForVideo(videoId: Video["id"]) {
  return dummyUploadSessions.filter((session) => session.videoId === videoId)
}

export function getDummyProcessingRunsForVideo(videoId: Video["id"]) {
  return dummyProcessingRuns.filter((run) => run.videoId === videoId)
}

export function getDummyRenditionsForVideo(videoId: Video["id"]) {
  return dummyVideoRenditions.filter((rendition) => rendition.videoId === videoId)
}
